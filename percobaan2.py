p = int(input("Masukan Panjang Balok: "))
l = int(input("Masukan Lebar Balok: "))
t = int(input("Masukan Tinggi Balok: "))
print("\nRumus Volume dari Bangun Ruang Balok: p x l x t\n")
volume_balok = p * l * t
print("P: %d" % (p))
print("L: %d" % (l))
print("T: %d" % (t))
print("Volume Bangun Ruang Balok tersebut adalah: %d" % (volume_balok))